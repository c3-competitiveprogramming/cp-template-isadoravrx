#include <iostream>
#include <cmath>
using namespace std;

int main(){
    double n,a,x,y;
    while(cin >> n >> a && (n + a) > 0){
        for(int i = 0; i < 1; i++){
            scanf("%lf %lf",&x,&y);

            d = sqrt(x * x + y * y);
            if(d > a){
                continue;
            }

        }
    }
}
